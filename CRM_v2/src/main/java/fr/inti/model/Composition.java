package fr.inti.model;


public class Composition {

	private int quantite;

	public Composition() {
		super();
	}

	public Composition(int quantite) {
		super();
		this.quantite = quantite;
	}

	public int getQuantite() {
		return quantite;
	}

	public void setQuantite(int quantite) {
		this.quantite = quantite;
	}

}
