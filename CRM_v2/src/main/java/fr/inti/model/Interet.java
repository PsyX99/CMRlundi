package fr.inti.model;

public class Interet {

	private double niveau;

	public Interet() {
		super();
	}

	public Interet(double niveau) {
		super();
		this.niveau = niveau;
	}

	public double getNiveau() {
		return niveau;
	}

	public void setNiveau(double niveau) {
		this.niveau = niveau;
	}

}
